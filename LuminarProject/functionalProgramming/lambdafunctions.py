# Lambda function for addition

f=lambda num1,num2: num1+num2
print(f(10,50))

#Lambda function for subtraction

f=lambda num1,num2:num1-num2
print(f(100,20))

mulfun=lambda num1,num2:num1*num2 #Lambda function for multiplication
print(mulfun(3,4))

divfun=lambda num1,num2:num1/num2 #Lambda function for division
print(divfun(10,5))

cubefun=lambda num1:num1**3 #Lambda function to find cube of a number
print(cubefun(3))